create database flamma;
use flamma;
#drop database flamma
create table departamento (
    id_departamento smallint not null primary key,
    nombre character varying(30) not null
);

create table ciudad(
    id_ciudad smallint not null primary key,
    id_departamento smallint not null,
    nombre character varying(40) not null,
    costo numeric not null,
    FOREIGN KEY (id_departamento) REFERENCES departamento (id_departamento)
);

create table cupon(
    id_cupon character varying(20) not null primary key,
    valor numeric(3,2) not null
);

create table clasificacion(
	id_clasficacion smallint not null primary key,
    nombre character varying(40) not null,
    imagen character varying(30) not null
);

create table categoria(
	id_categoria smallint not null primary key,
    FOREIGN KEY (id_categoria) REFERENCES clasificacion (id_clasficacion)
);

create table coleccion(
	id_coleccion smallint not null primary key,
    fecha date not null,
    FOREIGN KEY (id_coleccion) REFERENCES clasificacion (id_clasficacion)
);

create table talla(
    id_talla smallint not null primary key,
    nombre character varying(10) not null
);

create table producto(
    referencia character varying(5) not null primary key,
    nombre character varying(40) not null,
    costo numeric not null,
    descripcion character varying(200),
    categoria smallint not null,
    coleccion smallint not null,
    FOREIGN KEY (categoria) REFERENCES categoria (id_categoria),
    FOREIGN KEY (coleccion) REFERENCES coleccion (id_coleccion)
);

create table imagen_producto(
    nombre character varying(30) not null primary key,
    producto character varying(5) not null,
    FOREIGN KEY (producto) REFERENCES producto (referencia)
);

create table talla_producto(
	referencia character varying(5) not null,
    id_talla smallint not null,
    existencia smallint not null,
    primary key(referencia,id_talla),
    foreign key (referencia) references producto(referencia),
    foreign key (id_talla) references talla(id_talla)
);

create table pedido(
    numero smallint not null primary key,
    cliente varchar(60) not null,
    departamento smallint not null,
    ciudad smallint not null,
    direccion character varying(60) not null,
    especificacion varchar(30) default null,
    telefono varchar(10) not null,
    nro_seguimiento character varying(30),
    estado varchar(10) not null,
    descuento numeric not null,
    FOREIGN KEY (departamento) REFERENCES departamento (id_departamento),
    FOREIGN KEY (ciudad) REFERENCES ciudad (id_ciudad),
    check (estado='no enviado' or estado='enviado')
);

create table detalle_pedido(
	numero_pedido smallint not null,
    referencia character varying(5) not null,
    cantidad smallint not null,
    precio_unidad numeric not null,
    primary key(numero_pedido,referencia),
    FOREIGN KEY (numero_pedido) REFERENCES pedido (numero),
    FOREIGN KEY (referencia) REFERENCES producto (referencia)
);

